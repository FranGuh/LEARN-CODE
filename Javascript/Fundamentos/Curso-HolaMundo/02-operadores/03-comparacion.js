let a = 5;
// relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);


// de igualdad
console.log(a == 5); // coerción de tipos.
console.log(a == '5');
console.log(a != 5);
console.log(a !== '5');
console.log(a === 5); // tipo de dato y valor, no coerción de tipos.
console.log(Object.is(a,5));