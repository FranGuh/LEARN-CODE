let a = 5;
let b = 7;

console.log( a + b ); // expresión
console.log( a - b ); // resta
console.log( a * b ); // multiplicación
console.log( a / b ); // división
console.log( a % b ); // módulo
console.log( a ** b ); // potencia


// incrementar
console.log(++a);
console.log(a++);
console.log(a,'valor original')

// decrementar - decrement
console.log(--b);
console.log(b--);
console.log(b,'valor original')